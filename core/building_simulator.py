"""
基建模拟器模块 - 主程序
根据计算方法.md实现5步计算流程
"""

from .base_classes import 特殊变量存储器, 全局计数计算器
from .operator_classes import 迷迭香, 酒神, 弑君者, 黑键, 创建干员实例
from .station_calculator import 制造站效率计算器
from .trade_station_calculator import 贸易站效率计算器


class 基建模拟器:
    """
    基建模拟器 - 主程序
    计算流程（根据计算方法.md）：
    ① 收集所有干员的特殊变量声明（初始化特殊变量字典）
    ② 统计所有跨设施全局计数（宿舍总数、发电站数、贸易站数、派系人数等）
    ③ 第一轮遍历：计算所有特殊技能（产出原始变量值）
    ④ 第二轮遍历：计算全局变量转化（感知信息→思维链环/无声共鸣等）
    ⑤ 第三轮遍历：计算各设施效率（设施基础 + 干员效率）
    ⑥ 第四轮遍历：计算设施产出（制造、贸易、会客室等）
    """

    def __init__(self, 配置数据: dict):
        self.配置数据 = 配置数据
        self.特殊变量 = 特殊变量存储器()
        self.全局计数 = {}
        self.所有设施 = []  # 存储所有设施的计算器

    def 注册设施(self, 设施计算器, 设施名称: str = ""):
        """注册一个设施到模拟器"""
        设施计算器.设施名称 = 设施名称
        self.所有设施.append(设施计算器)

    def 步骤1_收集特殊变量声明(self):
        """步骤①: 遍历所有进驻干员，收集特殊变量到特殊变量列表"""
        for 设施 in self.所有设施:
            干员列表 = self._获取设施干员列表(设施)

            for op in 干员列表:
                name = op["名称"]
                try:
                    干员实例 = 创建干员实例(name, op["精英等级"], self.特殊变量, self.配置数据)
                    # 添加干员拥有的特殊变量到存储器（如果不存在则初始化）
                    for 变量名 in 干员实例.获取特殊变量列表():
                        if 变量名 not in self.特殊变量.变量字典:
                            self.特殊变量.变量字典[变量名] = 0
                            self.特殊变量.来源记录[变量名] = []
                except ValueError:
                    # 未知干员，跳过
                    pass

    def 步骤2_计算全局计数(self):
        """步骤②: 统计所有跨设施全局计数"""
        计数器 = 全局计数计算器(self.配置数据)
        self.全局计数 = 计数器.计算()

    def 步骤3_计算特殊技能(self):
        """步骤③: 第一轮遍历，计算所有特殊技能（产出原始变量值）"""
        for 设施 in self.所有设施:
            干员列表 = self._获取设施干员列表(设施)

            for op in 干员列表:
                name = op["名称"]
                try:
                    干员实例 = 创建干员实例(name, op["精英等级"], self.特殊变量, self.配置数据)

                    # 传入全局计数（供L2/L3/L4/L5依赖使用）
                    干员实例.设置全局计数(self.全局计数)

                    # 对于宿舍干员，根据干员类型设置不同参数
                    if hasattr(设施, '设施名称') and 设施.设施名称.startswith("宿舍"):
                        宿舍人数 = len(干员列表)
                        宿舍等级 = 设施.等级 if hasattr(设施, '等级') else 5
                        if hasattr(干员实例, '设置当前宿舍等级'):
                            干员实例.设置当前宿舍等级(宿舍等级)
                        elif hasattr(干员实例, '设置当前宿舍人数'):
                            干员实例.设置当前宿舍人数(宿舍人数)

                    干员实例.计算特殊技能()
                except ValueError:
                    # 未知干员，跳过
                    pass

    def 步骤4_计算全局变量转化(self):
        """步骤④: 第二轮遍历，计算全局变量转化（感知信息→思维链环/无声共鸣等）"""
        for 设施 in self.所有设施:
            干员列表 = self._获取设施干员列表(设施)

            for op in 干员列表:
                name = op["名称"]
                try:
                    干员实例 = 创建干员实例(name, op["精英等级"], self.特殊变量, self.配置数据)
                    if hasattr(干员实例, '计算感知信息转化'):
                        干员实例.计算感知信息转化()
                    # 其他转化方法可以在这里添加
                except ValueError:
                    pass

    def 步骤5_计算设施效率(self):
        """步骤⑤: 第三轮遍历，计算各设施效率（设施基础 + 干员效率）"""
        for 设施 in self.所有设施:
            # 将特殊变量和全局计数传递给设施计算器
            设施.特殊变量 = self.特殊变量
            设施.全局计数 = self.全局计数

    def 步骤6_计算设施产出(self) -> dict:
        """步骤⑥: 第四轮遍历，计算设施产出"""
        结果 = {}
        for 设施 in self.所有设施:
            # 使用设施名称作为key
            结果[设施.设施名称] = 设施.计算效率()
        return 结果

    def _获取设施干员列表(self, 设施) -> list:
        """获取设施的干员列表（活动室使用"使用干员"，其他设施使用"进驻干员"）"""
        if hasattr(设施, '使用干员') and 设施.使用干员:
            return [设施.使用干员]
        elif hasattr(设施, '进驻干员'):
            return 设施.进驻干员
        else:
            return []

    def 运行(self) -> dict:
        """运行完整计算流程（5步流程）"""
        # 步骤①: 收集特殊变量声明
        self.步骤1_收集特殊变量声明()

        # 步骤②: 计算全局计数
        self.步骤2_计算全局计数()

        # 步骤③: 计算特殊技能（产出原始变量）
        self.步骤3_计算特殊技能()

        # 步骤④: 计算全局变量转化
        self.步骤4_计算全局变量转化()

        # 步骤⑤: 计算设施效率
        self.步骤5_计算设施效率()

        # 步骤⑥: 计算设施产出
        return self.步骤6_计算设施产出()

    def 获取全局计数(self) -> dict:
        """获取全局计数结果（用于调试）"""
        return self.全局计数

    def 获取特殊变量(self) -> dict:
        """获取特殊变量结果（用于调试）"""
        return self.特殊变量.获取所有变量()
