"""
制造站效率计算器模块
支持归零类干员的特殊处理
"""

import math

from .base_classes import STATION_LEVEL_BONUS, STATION_LEVEL_CAPACITY, STATION_LEVEL_POWER, 特殊变量存储器
from .operator_classes import 创建干员实例


class 制造站效率计算器:
    """
    制造站效率计算器（支持归零类干员）
    输入：等级、干员列表、产物、仓库容量加成、特殊变量存储器（已预先计算好）、全局计数
    输出：设施基础效率 + 干员效率
    """

    def __init__(self, 等级: int, 进驻干员列表: list, 产物: str = "赤金", 仓库容量加成: int = 0,
                 特殊变量: 特殊变量存储器 = None, 全局计数: dict = None, 时间轴系统=None):
        self.等级 = 等级
        self.进驻干员 = 进驻干员列表
        self.产物 = 产物
        self.仓库容量加成 = 仓库容量加成
        self.特殊变量 = 特殊变量
        self.全局计数 = 全局计数
        self.时间轴系统 = 时间轴系统
        self.干员实例列表 = []

    def 初始化干员(self):
        """初始化干员实例"""
        for op in self.进驻干员:
            name = op["名称"]
            elite = op["精英等级"]
            # 使用注册表创建干员实例
            干员实例 = 创建干员实例(name, elite, self.特殊变量)
            # 传入全局计数
            if self.全局计数:
                干员实例.设置全局计数(self.全局计数)
            # 传入时间轴系统
            if self.时间轴系统:
                干员实例.设置时间轴系统(self.时间轴系统)
            # 设置站内干员数（供归零类干员使用）
            if hasattr(干员实例, '设置站内干员数'):
                干员实例.设置站内干员数(len(self.进驻干员))
            self.干员实例列表.append(干员实例)

    def 计算效率(self) -> dict:
        """计算设施效率：基础效率 + 干员效率（支持归零类干员）"""
        self.初始化干员()

        # 检查是否存在归零类干员
        归零干员列表 = []
        正常干员列表 = []

        for 干员 in self.干员实例列表:
            if hasattr(干员, '是否归零类') and 干员.是否归零类():
                归零干员列表.append(干员)
            else:
                正常干员列表.append(干员)

        详情 = []

        if 归零干员列表:
            # 存在归零类干员：正常干员效率归零，只计算归零类干员效率
            干员总效率 = 0.0
            for 干员 in 归零干员列表:
                效率 = 干员.计算效率()
                干员总效率 += 效率
                详情.append({
                    "干员": 干员.__class__.__name__,
                    "精英等级": 干员.精英等级,
                    "效率": math.floor(效率),
                    "基本属性": 干员.基本属性,
                    "类型": "归零类"
                })

            # 正常干员效率归零
            for 干员 in 正常干员列表:
                详情.append({
                    "干员": 干员.__class__.__name__,
                    "精英等级": 干员.精英等级,
                    "效率": 0,
                    "基本属性": 干员.基本属性,
                    "类型": "被归零"
                })

            归零标记 = True
        else:
            # 正常计算所有干员效率
            干员总效率 = 0.0
            for 干员 in self.干员实例列表:
                效率 = 干员.计算效率()
                干员总效率 += 效率
                详情.append({
                    "干员": 干员.__class__.__name__,
                    "精英等级": 干员.精英等级,
                    "效率": math.floor(效率),
                    "基本属性": 干员.基本属性,
                    "类型": "正常"
                })

            归零标记 = False

        # 设施基础效率 + 干员效率
        基础效率 = STATION_LEVEL_BONUS[self.等级]

        # 检查全局制造加成
        全局制造加成 = 0.0
        if self.特殊变量:
            全局制造加成 = self.特殊变量.获取变量("全局制造加成")

        总效率 = (基础效率 - 1) * 100 + 干员总效率 + 全局制造加成

        # 向下取整
        总效率取整 = math.floor(总效率)

        # 构建公式和计算过程
        if 归零标记:
            公式 = "总效率 = (基础效率 - 1) × 100% + 归零干员效率（其他干员归零）"
            计算过程 = f"({基础效率 * 100:.0f}% - 1) × 100% + {math.floor(干员总效率)}% [归零模式]"
        elif 全局制造加成 > 0:
            公式 = "总效率 = (基础效率 - 1) × 100% + 干员效率 + 全局制造加成"
            计算过程 = f"({基础效率 * 100:.0f}% - 1) × 100% + {math.floor(干员总效率)}% + {math.floor(全局制造加成)}%"
        else:
            公式 = "总效率 = (基础效率 - 1) × 100% + 干员效率"
            计算过程 = f"({基础效率 * 100:.0f}% - 1) × 100% + {math.floor(干员总效率)}%"

        return {
            "产物": self.产物,
            "制造站等级": self.等级,
            "基础效率": f"{基础效率 * 100:.0f}%",
            "基础容量": STATION_LEVEL_CAPACITY[self.等级],
            "电力消耗": STATION_LEVEL_POWER[self.等级],
            "干员总效率": f"{math.floor(干员总效率)}%",
            "总效率": f"{总效率取整}%",
            "归零模式": 归零标记,
            "公式": 公式,
            "计算过程": 计算过程,
            "仓库容量加成": self.仓库容量加成,
            "干员详情": 详情,
        }
