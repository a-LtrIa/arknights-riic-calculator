    贸易站 = 贸易站效率计算器(
        等级=3,
        进驻干员列表=config["贸易站1"]["进驻干员"],
        产物="龙门币"
    )

    模拟器.注册设施(制造站, "制造站1")
    模拟器.注册设施(贸易站, "贸易站1")

    # 运行模拟器
    结果 = 模拟器.运行()

    # 验证全局计数
    全局计数 = 模拟器.获取全局计数()
