    print(f"  - 宿舍总干员数: {全局计数['宿舍总干员数']} (期望: 5)")
    print(f"  - 发电站总数: {全局计数['发电站总数']} (期望: 2)")
    print(f"  - 贸易站总数: {全局计数['贸易站总数']} (期望: 1)")

    assert 全局计数['宿舍总干员数'] == 5, "宿舍总干员数错误"
    assert 全局计数['发电站总数'] == 2, "发电站总数错误"
    assert 全局计数['贸易站总数'] == 1, "贸易站总数错误"

    # 验证特殊变量
    特殊变量 = 模拟器.获取特殊变量()
    print(f"\n特殊变量:")
    print(f"  - 感知信息: {特殊变量.get('感知信息', 0)} (期望: 5)")
    print(f"  - 无声共鸣: {特殊变量.get('无声共鸣', 0)} (期望: >=5)")

