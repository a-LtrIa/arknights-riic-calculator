"""
明日方舟基建仿真计算器 - 入口文件
"""

import json
from core import (
    基建模拟器,
    制造站效率计算器,
    贸易站效率计算器,
    发电站效率计算器,
    会客室效率计算器,
    加工站效率计算器,
    办公室效率计算器,
    训练室效率计算器,
    宿舍效率计算器,
    控制中枢效率计算器,
    活动室效率计算器,
)


def main():
    # 从example_config.json读取配置数据
    with open('example_config.json', 'r', encoding='utf-8') as f:
        config = json.load(f)

    print("=" * 60)
    print("明日方舟基建 - 基建模拟器测试")
    print("=" * 60)

    # 创建基建模拟器
    模拟器 = 基建模拟器(config)

    # 遍历所有配置项，自动注册设施
    for 设施名, 设施数据 in config.items():
        if not isinstance(设施数据, dict):
            continue

        类型 = 设施数据.get("类型", "")
        等级 = 设施数据.get("等级", 1)
        产物 = 设施数据.get("产物", "")
        进驻干员 = 设施数据.get("进驻干员", [])
        氛围值 = 设施数据.get("氛围值", 0)


        if 产物:
            print(f"  产物: {产物}")
        if 进驻干员:
            print(f"  进驻干员:")
            for op in 进驻干员:
                print(f"    - {op['名称']} (精英{op['精英等级']})")

        # 根据类型注册设施
        设施 = None
        if 类型 == "制造站":
            设施 = 制造站效率计算器(
                等级=等级,
                进驻干员列表=进驻干员,
                产物=产物
            )
        elif 类型 == "贸易站":
            设施 = 贸易站效率计算器(
                等级=等级,
                进驻干员列表=进驻干员,
                产物=产物,
                配置数据=config,
                设施名称=设施名
            )
        elif 类型 == "发电站":
            设施 = 发电站效率计算器(
                等级=等级,
                进驻干员列表=进驻干员
            )
        elif 类型 == "会客室":
            设施 = 会客室效率计算器(
                等级=等级,
                进驻干员列表=进驻干员
            )
        elif 类型 == "加工站":
            设施 = 加工站效率计算器(
                等级=等级,
                进驻干员列表=进驻干员
            )
        elif 类型 == "办公室":
            设施 = 办公室效率计算器(
                等级=等级,
                进驻干员列表=进驻干员
            )
        elif 类型 == "训练室":
            设施 = 训练室效率计算器(
                等级=等级,
                进驻干员列表=进驻干员
            )
        elif 类型 == "宿舍":
            设施 = 宿舍效率计算器(
                等级=等级,
                进驻干员列表=进驻干员,
                氛围值=氛围值 if 氛围值 > 0 else None
            )
        elif 类型 == "控制中枢":
            设施 = 控制中枢效率计算器(
                等级=等级,
                进驻干员列表=进驻干员
            )
        elif 类型 == "活动室":
            使用干员 = 进驻干员[0] if 进驻干员 else None
            设施 = 活动室效率计算器(
                等级=等级,
                使用干员=使用干员,
                氛围值=氛围值
            )
        else:
            print(f"  (未知设施类型: {类型})")
            continue

        if 设施:
            模拟器.注册设施(设施, 设施名)

    # 运行模拟器
    结果 = 模拟器.运行()

    # 打印特殊变量
    print(f"\n{'=' * 60}")
    print("【特殊变量】")
    print("=" * 60)
    for 变量名, 值 in 模拟器.特殊变量.变量字典.items():
        print(f"  {变量名}: {值}")

    print(f"\n{'=' * 60}")
    print("【计算结果】")
    print("=" * 60)
    for 设施名, 数据 in 结果.items():
        print(f"\n{设施名}")
        if "制造站等级" in 数据:
            print(f"  设施类型: 制造站 Lv.{数据['制造站等级']}")
            print(f"  产物: {数据.get('产物', '-')}")
            print(f"  基础效率: {数据['基础效率']}")
            print(f"  基础容量: {数据['基础容量']}")
            print(f"  电力消耗: {数据['电力消耗']}")
            print(f"  干员总效率: {数据['干员总效率']}")
            print(f"  总效率: {数据['总效率']}")
        elif "贸易站等级" in 数据:
            print(f"  设施类型: 贸易站 Lv.{数据['贸易站等级']}")
            print(f"  产物: {数据.get('产物', '-')}")
            print(f"  基础效率: {数据['基础效率']}")
            print(f"  订单上限: {数据['订单上限']}")
            print(f"  电力消耗: {数据['电力消耗']}")
            print(f"  干员总效率: {数据['干员总效率']}")
            print(f"  总效率: {数据['总效率']}")
        elif "发电站等级" in 数据:
            print(f"  设施类型: 发电站 Lv.{数据['发电站等级']}")
            print(f"  提供电力: {数据['提供电力']}")
            print(f"  充能速度: {数据['总充能速度']}")
        elif "会客室等级" in 数据:
            print(f"  设施类型: 会客室 Lv.{数据['会客室等级']}")
            print(f"  提供好友位: {数据['提供好友位']}")
            print(f"  干员线索加成: {数据['干员线索加成']}")
            print(f"  电力消耗: {数据['电力消耗']}")
        elif "加工站等级" in 数据:
            print(f"  设施类型: 加工站 Lv.{数据['加工站等级']}")
            print(f"  干员副产物加成: {数据['干员副产物加成']}")
            print(f"  电力消耗: {数据['电力消耗']}")
        elif "办公室等级" in 数据:
            print(f"  设施类型: 办公室 Lv.{数据['办公室等级']}")
            print(f"  提供招募位: {数据['提供招募位']}")
            print(f"  联络速度: {数据['总联络速度']}")
            print(f"  电力消耗: {数据['电力消耗']}")
        elif "训练室等级" in 数据:
            print(f"  设施类型: 训练室 Lv.{数据['训练室等级']}")
            print(f"  专精等级上限: {数据['专精等级上限']}")
            print(f"  训练速度: {数据['总训练速度']}")
            print(f"  电力消耗: {数据['电力消耗']}")
        elif "宿舍等级" in 数据:
            print(f"  设施类型: 宿舍 Lv.{数据['宿舍等级']}")
            print(f"  心情恢复: {数据['总恢复速度']}")
            print(f"  氛围值: {数据['氛围值']}")
        elif "控制中枢等级" in 数据:
            print(f"  设施类型: 控制中枢 Lv.{数据['控制中枢等级']}")
            print(f"  心情减免: {数据['总心情减免']}")
            print(f"  进驻人员: {数据['进驻人员']}")
        elif "活动室等级" in 数据:
            print(f"  设施类型: 活动室 Lv.{数据['活动室等级']}")
            print(f"  使用干员: {数据['使用干员']}")
            print(f"  氛围值: {数据['当前氛围值']}/{数据['氛围值上限']}")
            print(f"  信赖加成: {数据['氛围加成']}")

        if '干员详情' in 数据 and 数据['干员详情']:
            print(f"\n  【干员详情】")
            for d in 数据['干员详情']:
                print(f"    {d['干员']}: 精英{d['精英等级']} = {d['效率']:.1f}%")
                print(f"      基本属性: {d['基本属性']}")

    # ==================== 简报 ====================
    print(f"\n{'=' * 60}")
    print("【效率简报】")
    print("=" * 60)

    for 设施名, 数据 in 结果.items():
        print(f"\n{设施名}:")
        if "制造站等级" in 数据:
            print(f"  公式: {数据['公式']}")
            print(f"  计算: {数据['计算过程']} = {数据['总效率']}")
            print(f"  干员构成:")
            for d in 数据.get('干员详情', []):
                if d['效率'] != 0:
                    print(f"    - {d['干员']}: +{d['效率']:.1f}% ({d['基本属性']})")
        elif "贸易站等级" in 数据:
            print(f"  公式: {数据['公式']}")
            print(f"  计算: {数据['计算过程']} = {数据['总效率']}")
            print(f"  干员构成:")
            for d in 数据.get('干员详情', []):
                if d['效率'] != 0:
                    print(f"    - {d['干员']}: +{d['效率']:.1f}% ({d['基本属性']})")
        elif "发电站等级" in 数据:
            print(f"  提供电力: {数据['提供电力']}")
            print(f"  充能速度: {数据['总充能速度']}")
        elif "宿舍等级" in 数据:
            print(f"  公式: 总恢复 = 基础恢复 + 氛围加成 + 干员加成")
            基础 = float(数据['基础心情恢复'].replace('/时', '').replace('+', ''))
            氛围 = float(数据['氛围加成'].replace('/时', '').replace('+', ''))
            干员 = float(数据['干员心情加成'].replace('/时', '').replace('+', ''))
            print(f"  计算: {基础:.2f} + {氛围:.2f} + {干员:.2f} = {基础 + 氛围 + 干员:.2f}/时")
            print(f"  干员构成:")
            for d in 数据.get('干员详情', []):
                if d['效率'] != 0:
                    print(f"    - {d['干员']}: +{d['效率']:.2f}/时 ({d['基本属性']})")
        elif "会客室等级" in 数据:
            print(f"  线索加成: {数据['干员线索加成']}")
            for d in 数据.get('干员详情', []):
                if d['效率'] != 0:
                    print(f"    - {d['干员']}: +{d['效率']:.0f}%")
        elif "加工站等级" in 数据:
            print(f"  副产品加成: {数据['干员副产物加成']}")
            for d in 数据.get('干员详情', []):
                if d['效率'] != 0:
                    print(f"    - {d['干员']}: +{d['效率']:.0f}% ({d['基本属性']})")
        elif "办公室等级" in 数据:
            print(f"  公式: 总联络速度 = 基础 + 干员加成")
            print(f"  计算: +{float(数据['基础联络速度'].replace('%', '')):.0f}% + {float(数据['干员联络加成'].replace('%', '')):.0f}% = {数据['总联络速度']}")
            print(f"  干员构成:")
            for d in 数据.get('干员详情', []):
                if d['效率'] != 0:
                    print(f"    - {d['干员']}: +{d['效率']:.0f}% ({d['基本属性']})")
        elif "训练室等级" in 数据:
            print(f"  训练速度: {数据['总训练速度']}")
            for d in 数据.get('干员详情', []):
                if d['效率'] != 0:
                    print(f"    - {d['干员']}: +{d['效率']:.0f}%")
        elif "控制中枢等级" in 数据:
            print(f"  心情减免: {数据['总心情减免']}")
            print(f"  进驻人员: {数据['进驻人员']}")
        elif "活动室等级" in 数据:
            print(f"  氛围值: {数据['当前氛围值']}/{数据['氛围值上限']}")
            print(f"  信赖加成: {数据['氛围加成']}")

    # ==================== 特殊变量简报 ====================
    print(f"\n{'=' * 60}")
    print("【特殊变量构成】")
    print("=" * 60)
    for 变量名, 值 in 模拟器.特殊变量.变量字典.items():
        if 值 > 0:
            print(f"\n  {变量名}: {值}")
            if 变量名 in 模拟器.特殊变量.来源记录:
                for 来源, 来源值 in 模拟器.特殊变量.来源记录[变量名]:
                    print(f"    - {来源} +{来源值}")


if __name__ == '__main__':
    main()
