class 迷迭香(干员基类):
    """
    明日方舟 迷迭香 基建技能类
    特殊变量: 感知信息, 思维链环
    """
    def __init__(self, 精英等级: int, 特殊变量: 特殊变量存储器, 配置数据: dict = None):
        super().__init__(精英等级, 特殊变量)
        self.配置数据 = 配置数据
        self.宿舍干员数量 = 0
        self.特殊变量列表 = ["感知信息", "思维链环"]  # 迷迭香拥有的特殊变量
        self.基本属性 = {
            "赤金效率": 0,
            "作战记录效率": 0,
            "仓库容量": 0,
        }
        # 如果传入了配置数据，自动计算宿舍干员数量
        if 配置数据:
            self.计算宿舍干员数量()

    def 计算宿舍干员数量(self):
        """从配置数据中计算宿舍总干员数"""
        if not self.配置数据:
            return
        count = 0
        for dorm_key in ["宿舍1", "宿舍2", "宿舍3", "宿舍4"]:
            if dorm_key in self.配置数据:
                count += len(self.配置数据[dorm_key]["进驻干员"])
        self.宿舍干员数量 = count

    def 计算特殊技能(self):
        """
        计算特殊技能：
        1. 感知信息增加：宿舍内每有1名干员则感知信息+1
        2. 思维链环：每1点感知信息转化为1点思维链环
        """
        # 感知信息增加
        if self.宿舍干员数量 > 0:
            self.特殊变量存储器.添加变量(
                "感知信息",
                self.宿舍干员数量,
                f"迷迭香(宿舍{self.宿舍干员数量}人)"
            )
        # 思维链环 = 感知信息
        感知信息 = self.特殊变量存储器.获取变量("感知信息")
        self.特殊变量存储器.添加变量(
            "思维链环",
            感知信息,
            "迷迭香(感知信息转化)"
        )

    def 计算效率(self) -> float:
        """
        计算效率：
        - 精英2: 思维链环/1 = +1% 每思维链环
        - 非精英2: 思维链环/2 = +0.5% 每思维链环
        """
        思维链环 = self.特殊变量存储器.获取变量("思维链环")
        if self.精英等级 == 2:
            return 思维链环 * 1.0  # 意识实体
        else:
            return 思维链环 / 2  # 念力


