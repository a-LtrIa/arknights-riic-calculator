# ====================== 制造站效率计算器 ======================

class 制造站效率计算器:
    """
    制造站效率计算器（只计算效率，不处理特殊技能）
    输入：等级、干员列表、产物、仓库容量加成、特殊变量存储器（已预先计算好）
    输出：设施基础效率 + 干员效率
    """

    def __init__(self, 等级: int, 进驻干员列表: list, 产物: str = "赤金", 仓库容量加成: int = 0, 特殊变量: 特殊变量存储器 = None):
        self.等级 = 等级
        self.进驻干员 = 进驻干员列表
        self.产物 = 产物
        self.仓库容量加成 = 仓库容量加成
        self.特殊变量 = 特殊变量
        self.干员实例列表 = []

    def 初始化干员(self):
        """初始化干员实例"""
        for op in self.进驻干员:
            name = op["名称"]
            elite = op["精英等级"]

            if name == "迷迭香":
                self.干员实例列表.append(迷迭香(elite, self.特殊变量))
            elif name == "酒神":
                self.干员实例列表.append(酒神(elite, self.特殊变量))
            elif name == "弑君者":
                self.干员实例列表.append(弑君者(elite, self.特殊变量))

    def 计算效率(self) -> dict:
        """计算设施效率：基础效率 + 干员效率"""
        self.初始化干员()

        干员总效率 = 0.0
        详情 = []
        for 干员 in self.干员实例列表:
            效率 = 干员.计算效率()
            干员总效率 += 效率
            详情.append({
                "干员": 干员.__class__.__name__,
                "精英等级": 干员.精英等级,
                "效率": 效率,
                "基本属性": 干员.基本属性
            })

        # 设施基础效率 + 干员效率
        # 基础效率是倍率(如1.08)，干员效率是百分比(如90)
        # 总效率 = (1.08-1)*100 + 90 = 8 + 90 = 98%
        基础效率 = STATION_LEVEL_BONUS[self.等级]
        总效率 = (基础效率 - 1) * 100 + 干员总效率

        return {
            "产物": self.产物,
            "制造站等级": self.等级,
            "基础效率": f"{基础效率 * 100:.0f}%",
            "基础容量": STATION_LEVEL_CAPACITY[self.等级],
            "电力消耗": STATION_LEVEL_POWER[self.等级],
            "干员总效率": f"{干员总效率:.1f}%",
            "总效率": f"{总效率:.1f}%",
            "仓库容量加成": self.仓库容量加成,
            "干员详情": 详情,
        }


# ====================== 主程序：基建模拟器 ======================

class 基建模拟器:
    """
    基建模拟器 - 主程序
    计算流程（根据计算方法.md）：
    1. 遍历所有进驻干员的特殊变量属性，添加到特殊变量列表
    2. 遍历所有设施，计算一遍特殊技能（目的是先计算全局变量）
    3. 再遍历一遍所有设施，计算效率（设施基础效率+干员效率）
    4. 最后计算产出
    """

    def __init__(self, 配置数据: dict):
        self.配置数据 = 配置数据
        self.特殊变量 = 特殊变量存储器()
        self.所有设施 = []  # 存储所有设施的计算器

    def 注册设施(self, 设施计算器):
        """注册一个设施到模拟器"""
        self.所有设施.append(设施计算器)

    def 收集特殊变量(self):
        """步骤1: 遍历所有进驻干员，收集特殊变量到特殊变量列表"""
        for 设施 in self.所有设施:
            for op in 设施.进驻干员:
                name = op["名称"]
                if name == "迷迭香":
                    迷迭香实例 = 迷迭香(op["精英等级"], self.特殊变量, self.配置数据)
                    # 添加迷迭香的特殊变量到存储器
                    for 变量名 in 迷迭香实例.获取特殊变量列表():
                        if 变量名 not in self.特殊变量.变量字典:
                            self.特殊变量.变量字典[变量名] = 0
                            self.特殊变量.来源记录[变量名] = []

    def 计算所有特殊技能(self):
        """步骤2: 遍历所有设施，计算一遍特殊技能（目的是先计算全局变量）"""
        for 设施 in self.所有设施:
            for op in 设施.进驻干员:
                name = op["名称"]
                if name == "迷迭香":
                    迷迭香实例 = 迷迭香(op["精英等级"], self.特殊变量, self.配置数据)
                    迷迭香实例.计算特殊技能()

    def 计算所有效率(self):
        """步骤3: 遍历所有设施，计算效率（设施基础效率+干员效率）"""
        for 设施 in self.所有设施:
            设施.特殊变量 = self.特殊变量

    def 计算产出(self):
        """步骤4: 最后计算产出"""
        结果 = {}
        for 设施 in self.所有设施:
            结果[设施.产物] = 设施.计算效率()
        return 结果

    def 运行(self) -> dict:
        """运行完整计算流程"""
        # 步骤1: 收集特殊变量
        self.收集特殊变量()

        # 步骤2: 计算特殊技能
        self.计算所有特殊技能()

        # 步骤3: 计算效率
        self.计算所有效率()

        # 步骤4: 计算产出
        return self.计算产出()


