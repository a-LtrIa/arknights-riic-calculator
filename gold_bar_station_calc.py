"""
明日方舟基建 - 三级赤金制造站仿真计算器
制造站等级: 3
进驻干员: ["迷迭香", "酒神", "弑君者"]
"""

from gold_bar_operators import 获取制造站干员, 加成结果
import json

# 基础数据
制造站等级 = 3
进驻干员列表 = ["迷迭香", "酒神", "弑君者"]

# 等级基础加成 (制造站)
等级基础加成 = {
    1: 1.00,
    2: 1.03,
    3: 1.08,
    4: 1.13,
    5: 1.20,
}

# 赤金制造基础数据
赤金基础时间 = 72  # 分钟/份
赤金仓库容量 = 18  # 基础仓库容量

def 计算干员加成(干员名称: str, 上下文: dict = None) -> 加成结果:
    """计算单个干员加成"""
    干员 = 获取制造站干员(干员名称)
    if 干员:
        return 干员.计算加成(上下文)
    return 加成结果(干员名称=干员名称)

def 计算总加成(干员列表: list, 上下文: dict = None) -> dict:
    """计算所有干员总加成"""
    总加成 = {
        "赤金加成": 0.0,
        "作战记录加成": 0.0,
        "源石加成": 0.0,
        "通用加成": 0.0,
        "心情修正": 0.0,
    }

    for 干员名 in 干员列表:
        加成 = 计算干员加成(干员名, 上下文)
        总加成["赤金加成"] += 加成.赤金加成
        总加成["作战记录加成"] += 加成.作战记录加成
        总加成["源石加成"] += 加成.源石加成
        总加成["通用加成"] += 加成.通用加成
        总加成["心情修正"] += 加成.心情修正

    return 总加成

def 计算赤金产量(干员列表: list, 等级: int, 上下文: dict = None) -> dict:
    """计算赤金制造站产量"""
    # 1. 计算干员加成
    总加成 = 计算总加成(干员列表, 上下文)

    # 2. 计算总生产力倍率
    基础加成 = 等级基础加成[等级]  # 等级基础加成
    干员通用 = 总加成["通用加成"] / 100.0  # 通用加成转倍率
    干员赤金 = 总加成["赤金加成"] / 100.0  # 赤金专用加成转倍率
    总倍率 = 基础加成 * (1 + 干员通用 + 干员赤金)

    # 3. 计算生产时间和产量
    单份时间 = 赤金基础时间 / 总倍率  # 分钟/份
    每小时产量 = 60 / 单份时间  # 份/小时
    每日产量 = 每小时产量 * 24  # 份/天

    # 4. 计算心情消耗影响 (简化计算)
    心情修正 = 总加成["心情修正"]
    心情消耗率 = 1.0 + 心情修正  # 正值增加消耗，负值减少消耗

    return {
        "制造站等级": 等级,
        "进驻干员": 干员列表,
        "等级基础加成": f"{基础加成 * 100:.0f}%",
        "通用加成": f"{总加成['通用加成']:.1f}%",
        "赤金专用加成": f"{总加成['赤金加成']:.1f}%",
        "心情修正": f"{心情修正:+.2f}",
        "总生产力倍率": f"{总倍率 * 100:.2f}%",
        "单份生产时间": f"{单份时间:.1f} 分钟",
        "每小时产量": f"{每小时产量:.2f} 份",
        "每日产量": f"{每日产量:.1f} 份",
        "心情消耗率": f"{心情消耗率:.2f}",
    }

def 打印结果(结果: dict):
    """打印计算结果"""
