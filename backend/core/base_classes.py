# ====================== 特殊变量存储器 ======================
class 特殊变量存储器:
    """动态存储所有干员的特殊变量"""
    def __init__(self):
        self.变量字典 = {}  # {"感知信息": 0, "思维链环": 0, ...}
        self.来源记录 = {}  # {"感知信息": [("来源1", 值1), ("来源2", 值2), ...], ...}

    def 添加变量(self, 变量名: str, 值: float, 来源: str = ""):
        """添加或更新特殊变量"""
        if 变量名 not in self.变量字典:
            self.变量字典[变量名] = 0
            self.来源记录[变量名] = []
        self.变量字典[变量名] += 值
        if 来源:
            self.来源记录[变量名].append((来源, 值))

    def 获取变量(self, 变量名: str) -> float:
        """获取特殊变量值"""
        return self.变量字典.get(变量名, 0)

    def 获取所有变量(self) -> dict:
        """获取所有特殊变量"""
        return self.变量字典.copy()


# ====================== 干员基建技能类 ======================
class 心情系统:
    """管理所有干员的心情状态"""
    def __init__(self, 配置数据: dict = None):
        # 默认心情上限为24（满心情）
        self.心情上限 = 24
        # 所有干员默认心情为满
        self.干员心情 = {}  # {"干员名称": 心情值}

        if 配置数据:
            self.初始化干员心情(配置数据)

    def 初始化干员心情(self, 配置数据: dict):
        """从配置数据初始化干员心情，默认为满"""
        for 设施名, 设施数据 in 配置数据.items():
            if isinstance(设施数据, dict) and "进驻干员" in 设施数据:
                for op in 设施数据["进驻干员"]:
                    name = op["名称"]
                    if name not in self.干员心情:
                        self.干员心情[name] = self.心情上限

    def 获取心情(self, 干员名称: str) -> float:
        """获取干员心情，默认返回满心情"""
        return self.干员心情.get(干员名称, self.心情上限)

    def 设置心情(self, 干员名称: str, 心情值: float):
        """设置干员心情"""
        self.干员心情[干员名称] = min(心情值, self.心情上限)

    def 消耗心情(self, 干员名称: str, 消耗量: float) -> float:
        """消耗心情，返回消耗后剩余心情"""
        当前心情 = self.获取心情(干员名称)
        新心情 = max(0, 当前心情 - 消耗量)
        self.干员心情[干员名称] = 新心情
        return 新心情


class 时间轴系统:
    """管理干员的工作时间和设施运行时间"""
    def __init__(self, 配置数据: dict = None):
        # 当前时间（小时），从0开始
        self.当前时间 = 0.0
        # 干员工作时间记录 {"干员名称": 开始时间}
        self.干员工作时间 = {}
        # 设施运行时间记录 {"设施名": 开始时间}
        self.设施运行时间 = {}

        if 配置数据:
            self.初始化时间(配置数据)

    def 初始化时间(self, 配置数据: dict):
        """从配置数据初始化干员和设施的开始时间，默认为0"""
        for 设施名, 设施数据 in 配置数据.items():
            if isinstance(设施数据, dict):
                # 记录设施开始运行时间
                if 设施名 not in self.设施运行时间:
                    self.设施运行时间[设施名] = 0.0
                # 记录干员开始工作时间
                if "进驻干员" in 设施数据:
                    for op in 设施数据["进驻干员"]:
                        name = op["名称"]
                        if name not in self.干员工作时间:
                            self.干员工作时间[name] = 0.0

    def 设置当前时间(self, 时间: float):
        """设置当前时间（小时）"""
        self.当前时间 = 时间

    def 获取当前时间(self) -> float:
        """获取当前时间（小时）"""
        return self.当前时间

    def 获取干员工作时长(self, 干员名称: str) -> float:
        """获取干员已工作时长（小时）"""
        开始时间 = self.干员工作时间.get(干员名称, 0.0)
        return self.当前时间 - 开始时间

    def 获取设施运行时长(self, 设施名: str) -> float:
        """获取设施已运行时长（小时）"""
        开始时间 = self.设施运行时间.get(设施名, 0.0)
        return self.当前时间 - 开始时间

    def 重置干员工作时间(self, 干员名称: str):
        """重置干员工作时间（如换班时）"""
        self.干员工作时间[干员名称] = self.当前时间

    def 重置设施运行时间(self, 设施名: str):
        """重置设施运行时间"""
        self.设施运行时间[设施名] = self.当前时间


